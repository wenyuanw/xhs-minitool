def build_items():
    seen = set()
    items = []
    for row in ROWS:
        (emoji, name_zh, name_en, category, popularity, tone, meaning_xhs, usage_tips,
         verticals, position, combos, alts, examples, tags) = row
        key = emoji
        if key in seen:
            continue
        seen.add(key)
        items.append({
            "emoji": emoji,
            "name_zh": name_zh,
            "name_en": name_en,
            "category": category,
            "tone": tone,
            "meaning_xhs": meaning_xhs,
            "usage_tips": usage_tips,
            "verticals": verticals,
            "position": position,
            "common_combinations": [c for c in combos if c != emoji][:4],
            "alternatives": [a for a in alts if a != emoji][:4],
            "examples": examples,
            "popularity": popularity,
            "tags": tags,
        })
    items.sort(key=lambda x: (-x["popularity"], x["category"], x["name_zh"]))
    return items


def main():
    items = build_items()
    out = Path(__file__).resolve().parents[1] / "src" / "data" / "emojis.json"
    out.write_text(json.dumps(items, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    from collections import Counter
    c = Counter(i["category"] for i in items)
    print(f"Wrote {len(items)} emojis -> {out}")
    print(dict(c))


if __name__ == "__main__":
    main()
